#!/usr/bin/env zx
/**
 * shared/validation-rules.json (SSOT) から以下の2ファイルを自動生成する。
 *   1. apps/backend/config/validation.py   — Python 定数
 *   2. packages/types/src/validation-rules.generated.ts — TypeScript 定数
 *
 * Usage:
 *   node scripts/generate-validation.mjs          # 生成
 *   node scripts/generate-validation.mjs --check   # 差分チェック（CI用、差分があれば exit 1）
 */
import "zx/globals";
import path from "node:path";
import { readFile, writeFile } from "node:fs/promises";
import { getProjectRoot } from "./utils.mjs";

const projectRoot = getProjectRoot(import.meta.url);

const SSOT_PATH = path.join(projectRoot, "shared", "validation-rules.json");
const PY_OUT = path.join(projectRoot, "apps", "backend", "config", "validation.py");
const TS_OUT = path.join(projectRoot, "packages", "types", "src", "validation-rules.generated.ts");

const isCheck = process.argv.includes("--check");

// ---------- helpers ----------

/** camelCase → UPPER_SNAKE_CASE  (e.g. "reportTitle" → "REPORT_TITLE") */
function camelToUpperSnake(s) {
  return s.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase();
}

/**
 * Build a Python constant name that avoids collisions across entities.
 * Uses the entity prefix only when the field name alone would collide.
 *
 * Collision detection is done by scanning all entities beforehand.
 */
function buildPyConstantName(entity, field, collisions) {
  const base = camelToUpperSnake(field);
  if (collisions.has(field)) {
    return `MAX_${camelToUpperSnake(entity)}_${base}_LENGTH`;
  }
  return `MAX_${base}_LENGTH`;
}

// ---------- load SSOT ----------

const raw = await readFile(SSOT_PATH, "utf8");
const rules = JSON.parse(raw);

// ---------- generate Python ----------

function generatePython(rules) {
  const lines = [
    '"""',
    "AUTO-GENERATED by scripts/generate-validation.mjs — DO NOT EDIT.",
    "",
    "Source of truth: shared/validation-rules.json",
    '"""',
    "",
  ];

  // Emit MAX_*_LENGTH constants grouped by entity
  const entityOrder = [
    "report",
    "company",
    "worker",
    "instrument",
    "part",
    "schemaDefinition",
    "site",
    "targetInstrument",
    "tableDefinition",
    "targetInstrumentTable",
  ];
  const entityLabels = {
    report: "レポート",
    company: "会社マスタ",
    worker: "作業者マスタ",
    instrument: "計器マスタ",
    part: "部品マスタ",
    schemaDefinition: "スキーマ定義",
    site: "現場",
    targetInstrument: "対象計器",
    tableDefinition: "表定義マスタ",
    targetInstrumentTable: "対象機器・表",
  };

  // Detect field names that appear in multiple entities → need entity prefix
  const fieldCount = {};
  for (const entity of entityOrder) {
    const fields = rules[entity];
    if (!fields) continue;
    for (const field of Object.keys(fields)) {
      fieldCount[field] = (fieldCount[field] || 0) + 1;
    }
  }
  const collisions = new Set(
    Object.entries(fieldCount)
      .filter(([, n]) => n > 1)
      .map(([k]) => k)
  );

  for (const entity of entityOrder) {
    const fields = rules[entity];
    if (!fields) continue;
    lines.push(`# ${entityLabels[entity] ?? entity}`);
    for (const [field, rule] of Object.entries(fields)) {
      if (rule.maxLength != null) {
        const constName = buildPyConstantName(entity, field, collisions);
        lines.push(`${constName} = ${rule.maxLength}`);
      }
    }
    lines.push("");
  }

  // Emit regex patterns
  if (rules.patterns) {
    lines.push("# 正規表現パターン");
    for (const [name, pattern] of Object.entries(rules.patterns)) {
      const constName = `PATTERN_${camelToUpperSnake(name)}`;
      lines.push(`${constName} = r"${pattern}"`);
    }
    lines.push("");
  }

  lines.push(
    "# 役割キーは Scout が Dexie（IndexedDB）の既存値から動的生成する。フロント側で新規入力も許可。"
  );
  lines.push("");

  return lines.join("\n");
}

// ---------- generate TypeScript ----------

function generateTypeScript(rules) {
  const lines = [
    "/**",
    " * AUTO-GENERATED by scripts/generate-validation.mjs — DO NOT EDIT.",
    " *",
    " * Source of truth: shared/validation-rules.json",
    " */",
    "",
    "// eslint-disable-next-line @typescript-eslint/no-unused-vars -- re-exported for consumers",
    "export const VALIDATION_RULES = {",
  ];

  const entityOrder = [
    "report",
    "company",
    "worker",
    "instrument",
    "part",
    "schemaDefinition",
    "site",
    "targetInstrument",
    "tableDefinition",
    "targetInstrumentTable",
  ];

  for (const entity of entityOrder) {
    const fields = rules[entity];
    if (!fields) continue;
    lines.push(`  ${entity}: {`);
    for (const [field, rule] of Object.entries(fields)) {
      const parts = [];
      if (rule.maxLength != null) parts.push(`maxLength: ${rule.maxLength}`);
      if (rule.required) parts.push(`required: true as const`);
      lines.push(`    ${field}: { ${parts.join(", ")} },`);
    }
    lines.push("  },");
  }

  lines.push("} as const;");
  lines.push("");

  // Patterns as RegExp
  if (rules.patterns) {
    lines.push("export const PATTERNS = {");
    for (const [name, pattern] of Object.entries(rules.patterns)) {
      lines.push(`  ${name}: /${pattern}/,`);
    }
    lines.push("} as const;");
    lines.push("");
  }

  return lines.join("\n");
}

// ---------- write or check ----------

const pyContent = generatePython(rules);
const tsContent = generateTypeScript(rules);

if (isCheck) {
  let fail = false;

  try {
    const existing = await readFile(PY_OUT, "utf8");
    // Normalize line endings for comparison
    if (existing.replace(/\r\n/g, "\n") !== pyContent.replace(/\r\n/g, "\n")) {
      console.error(
        `MISMATCH: ${path.relative(projectRoot, PY_OUT)} — run: node scripts/generate-validation.mjs`
      );
      fail = true;
    }
  } catch {
    console.error(
      `MISSING: ${path.relative(projectRoot, PY_OUT)} — run: node scripts/generate-validation.mjs`
    );
    fail = true;
  }

  try {
    const existing = await readFile(TS_OUT, "utf8");
    if (existing.replace(/\r\n/g, "\n") !== tsContent.replace(/\r\n/g, "\n")) {
      console.error(
        `MISMATCH: ${path.relative(projectRoot, TS_OUT)} — run: node scripts/generate-validation.mjs`
      );
      fail = true;
    }
  } catch {
    console.error(
      `MISSING: ${path.relative(projectRoot, TS_OUT)} — run: node scripts/generate-validation.mjs`
    );
    fail = true;
  }

  if (fail) {
    process.exit(1);
  } else {
    console.log("Validation files are up to date.");
  }
} else {
  await writeFile(PY_OUT, pyContent, "utf8");
  console.log(`  Generated: ${path.relative(projectRoot, PY_OUT)}`);
  await writeFile(TS_OUT, tsContent, "utf8");
  console.log(`  Generated: ${path.relative(projectRoot, TS_OUT)}`);
}
